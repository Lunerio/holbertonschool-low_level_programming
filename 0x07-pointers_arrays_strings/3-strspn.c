unsigned int _strspn(char *s, char *accept)
{
	unsigned int i;
	unsigned int j;

	for (i = 0; s[i] != '\0'; i++)
	{
		for (j = 0; j < i; j++)
		if (s == accept)
		{
			break;
		}
	}
	return (j);
}
